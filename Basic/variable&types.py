"""
English Version
Variables and Data Types in Python
- Variables are used to store data values.
- Python has no command for declaring a variable.
- A variable is created the moment you first assign a value to it.
- Variables do not need to be declared with any particular type and can even change type after they have been set.
- Variable names can be any length and can consist of letters, numbers, and underscores.
- Variable names must start with a letter or an underscore (_), followed by letters, numbers, or underscores.
"""
"""
Español Version
Variables y Tipos de Datos en Python
- Las variables se utilizan para almacenar valores de datos.
- Python no tiene un comando para declarar una variable.
- Una variable se crea en el momento en que le asignas un valor por primera vez.
- Las variables no necesitan ser declaradas con ningún tipo particular y pueden incluso cambiar de tipo después de haber sido establecidas.
- Los nombres de las variables pueden tener cualquier longitud y pueden consistir en letras, números y guiones bajos.
- Los nombres de las variables deben comenzar con una letra o un guion bajo (_), seguido de letras, números o guiones bajos.
- Los nombres de las variables no pueden comenzar con un número.
- Los nombres de las variables no pueden contener espacios.
- Los nombres de las variables no pueden contener caracteres especiales como @, #, $, %, etc.
- Los nombres de las variables son sensibles a mayúsculas y minúsculas, lo que significa que "variable" y "Variable" se consideran diferentes variables.
"""

